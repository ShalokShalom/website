Ekipa Subsurface z przyjemnością ogłasza udostępnienie najnowszej wersji Subsurface.

Zmiany w wersji  3.1.1 (w porównaniu do Subsurface 3.1):
<ul>
	<li>wsparcie dla aktualnego firmware w komputerach OSTC2 i OSTC3</li>
	<li>poprawka eksportu danych do divelog.de z komputerów Mac</li>
	<li>dodana obsługa importu Aquadivelog UDDF</li>
	<li>dodano opcje linii poleceń --version oraz --help</li>
	<li>zaktualizowana dokumentacja</li>
	<li>zaktualizowane tłumaczenia (w tym dodanie nowego języka - wietnamski)</li>
</ul><